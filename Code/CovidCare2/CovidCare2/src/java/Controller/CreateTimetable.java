    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
                TimetableEvent timetableEvent = new TimetableEvent();
        String username = request.getParameter("username");
        timetableEvent.setUsername(username);
        timetableEvent.setTitle(request.getParameter("action"));
        timetableEvent.setStart(Timestamp.valueOf(request.getParameter("start").replace("T", " ")));
        timetableEvent.setEnd(Timestamp.valueOf(request.getParameter("end").replace("T", " ")));
        TimetableDAO timetableDAO = new TimetableDAO();
        timetableDAO.createEvent(timetableEvent, username);
        response.sendRedirect("timetable");
    }

